    private void jButtonValidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonValidateActionPerformed
        int contadorCorrectas = 0;
        //-------------------------
        //COMPROBAR NOMBRE
        //-------------------------
        String name = jTextFieldName.getText();
        if (UserDataValidations.checkName(name)) {
            contadorCorrectas++;
            jLabelErrorName.setText("Nombre Correcto");
            jLabelErrorName.setVisible(true);
            jLabelErrorName.setForeground(Color.getHSBColor((float) 0.33, (float) 0.5, (float) 0.7));
            jTextFieldName.setEnabled(false);
        } else {

            jLabelErrorName.setText("Nombre Incorrecto");
            jLabelErrorName.setVisible(true);
            jLabelErrorName.setForeground(Color.RED);
        }
        //-------------------------
        //COMPROBAR FECHA
        //-------------------------

        String date = jTextFieldDate.getText();
boolean dateCorrecta = false;

try {
    UserDataValidations.checkDate(date);
    jLabelErrorDate.setText("Fecha válida");
    jLabelErrorDate.setVisible(true);
    jLabelErrorDate.setForeground(Color.getHSBColor(0.33f, 0.5f, 0.7f));
    dateCorrecta = true;
} catch (InvalidDateException e) {
    jLabelErrorDate.setText("Fecha incorrecta: " + e.getMessage());
    jLabelErrorDate.setVisible(true);
    jLabelErrorDate.setForeground(Color.RED);
}
if (nifCorrecto &&
    dateCorrecta &&
    UserDataValidations.checkZIP(cp) &&
    UserDataValidations.checkName(name) &&
    UserDataValidations.checkEmail(email)) {

    jButtonCSV.setEnabled(true);
    jButtonValidate.setEnabled(false);
} else {
    jButtonCSV.setEnabled(false);
    jButtonValidate.setEnabled(true);
}

        //-------------------------
        //COMPROBAR EMAIL
        //-------------------------

        String email = jTextFieldEmail.getText();
        if (UserDataValidations.checkEmail(email)) {
            contadorCorrectas++;
            jTextFieldEmail.setEnabled(false);

            jLabelErrorEmail.setText("Email Correcto");
            jLabelErrorEmail.setVisible(true);
            jLabelErrorEmail.setForeground(Color.getHSBColor((float) 0.33, (float) 0.5, (float) 0.7));
        } else {
            jLabelErrorEmail.setText("Email Incorrecto");
            jLabelErrorEmail.setVisible(true);
            jLabelErrorEmail.setForeground(Color.RED);
        }

        //-------------------------
        //COMPROBAR CP
        //-------------------------
        String cp = jTextFieldCP.getText().trim();
        if (UserDataValidations.checkZIP(cp)) {
            contadorCorrectas++;
            jTextFieldCP.setEnabled(false);

            jLabelErrorCP.setText("Código postal Correcto");
            jLabelErrorCP.setVisible(true);
            jLabelErrorCP.setForeground(Color.getHSBColor((float) 0.33, (float) 0.5, (float) 0.7));
        } else {
            jLabelErrorCP.setText("Código Postal Incorrecta");
            jLabelErrorCP.setVisible(true);
            jLabelErrorCP.setForeground(Color.RED);
        }

//-------------------------
// COMPROBAR NIF
//-------------------------
        String nif = jTextFieldNif.getText();
        boolean nifCorrecto = false;

        try {
            UserDataValidations.checkId(nif); // Si es inválido, lanza la excepción
            contadorCorrectas++;
            jTextFieldNif.setEnabled(false);
            jLabelErrorNif.setText("Documento Correcto");
            jLabelErrorNif.setVisible(true);
            jLabelErrorNif.setForeground(Color.getHSBColor((float) 0.33, (float) 0.5, (float) 0.7));
            nifCorrecto = true;
        } catch (InvalidDNIException e) {
            jLabelErrorNif.setText("Documento Incorrecto: " + e.getMessage());
            jLabelErrorNif.setVisible(true);
            jLabelErrorNif.setForeground(Color.RED);
        }

// Solo si el NIF y el resto de validaciones son correctas, se activan los botones
        if (nifCorrecto
                && UserDataValidations.checkZIP(cp)
                && UserDataValidations.checkName(name)
                && UserDataValidations.checkEmail(email)
                && UserDataValidations.checkDate(date)) {
            jButtonCSV.setEnabled(true);
            jButtonValidate.setEnabled(false);
        } else {
            jButtonCSV.setEnabled(false);
            jButtonValidate.setEnabled(true);
        }

    }//GEN-LAST:event_jButtonValidateActionPerformed

