    private void jButtonCSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCSVActionPerformed
        jButtonValidate.setEnabled(true);
        jButtonClear.setEnabled(true);
        
        String filePath = "datos.csv";
        File file = new File(filePath);    
    try {
        FileWriter fw = new FileWriter(file, true);
        PrintWriter pw = new PrintWriter(fw);
        
        pw.println(jTextFieldName.getText() + "," + jTextFieldNif.getText() + "," + jTextFieldEmail.getText() + "," + jTextFieldDate.getText() + "," + jTextFieldCP.getText());
        
        pw.close();
        fw.close();
        
        JOptionPane.showMessageDialog(this, "Datos guardados en CSV", "Éxito", JOptionPane.INFORMATION_MESSAGE);
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error al guardar en CSV", "Error", JOptionPane.ERROR_MESSAGE);
    }


    }//GEN-LAST:event_jButtonCSVActionPerformed

